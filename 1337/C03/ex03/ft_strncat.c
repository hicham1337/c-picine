char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	char	*d;
	char	*s;

	d = dest;
	s = src;
	while (*d != '\0')
	{
		d++;
	}
	while (*s != '\0' && nb != 0)
	{
		*d = *s;
		s++;
		d++;
		--nb;
	}
	*d = '\0';
	return (dest);
}
